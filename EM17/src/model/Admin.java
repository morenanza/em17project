package model;

public class Admin {
    private String username;
    private String password;
    public void setUsername(String user)
    {
        username = user;
    }
    public void setPassword(String pass)
    {
        password = pass;
    }
    public String getUsername()
    {
        return username;
    }
    public String getPassword()
    {
        return password;
    }
}
